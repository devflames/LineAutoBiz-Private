<h2 class="popTtl_icon"><span><img src="{{ asset("/images/icon_form.gif") }}" width="40" height="40" alt=""/></span><em>メルマガ機能一覧</em></h2>


<dl class="list_Box list_Box1">
<dt>セグメント配信機能</dt>
<dd>性別や地域など、配信対象を絞り込んでメール配信が行えます。</dd>
</dl>


<dl class="list_Box">
<dt>バックナンバー機能</dt>
<dd>過去に配信したメルマガを一覧で公開できます（非公開設定も可）。<br>
バックナンバーの公開は、メルマガ単位、各メール単位で設定可能です。パスワードを設定して特定の人だけに公開することもできます。
</dd>
</dl>


<dl class="list_Box">
<dt>バースデーメール機能</dt>
<dd>登録者の誕生日や記念日などを基準にしたメール配信が行えます。</dd>
</dl>


<dl class="list_Box">
<dt>イベントメール機能</dt>
<dd>キャンペーンやイベント日など、特定の日付を基準にしたメール配信が行えます。</dd>
</dl>



<dl class="list_Box">
<dt>入力内容自動差込</dt>
<dd>件名や本文にお名前やメールアドレス等、フォームに入力された内容を差し込めます。</dd>
</dl>


<dl class="list_Box">
<dt>定型文挿入機能</dt>
<dd>いつもメルマガに入れる決まった文章を登録し、メール内に差し込むことができます。</dd>
</dl>


<dl class="list_Box">
<dt>配信解除URL差し込み</dt>
<dd>読者側で配信解除ができる解除URLをメール内に差し込むことができます。</dd>
</dl>


<dl class="list_Box">
<dt>HTMLメール配信</dt>
<dd>文字の色やサイズを変えたり、画像を表示させたり、HTML形式のメールを送れます。<br>
テキスト形式メールも用意しておくことで、HTML形式メールを表示できない環境の人には、テキスト形式で表示される「マルチパート形式」を採用しています。<br>
<span class="note">※デコメールの配信には対応していません。</span></dd>
</dl>

<dl class="list_Box">
<dt>開封率測定</dt>
<dd>メルマガの開封率と「誰が」開封したのかを調べられます。</dd>
</dl>

<dl class="list_Box">
<dt>エラーメール自動処理機能</dt>
<dd>エラー内容によって解除・停止を自動で処理します。</dd>
</dl>


<dl class="list_Box">
<dt>多言語対応</dt>
<dd>メール配信の文字コードをISO-2022-JP、Unicode(UTF-8）から選択できますので、中国語など、他の言語でのメールも送れます。</dd>
</dl>

<dl class="list_Box">
<dt>閲覧制限つきメルマガ</dt>
<dd>個人情報にアクセスさせずにメルマガを配信できる、専用のメルマガ発行画面があります。</dd>
</dl>



