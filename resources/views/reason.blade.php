<p class="boxTxt mgB15">オートビズは、『メールを確実に届ける』という当たり前で難しいことを実現し続けるために、様々な取り組みを行なっています。</p>
<h4 class="boxTtlS">嫌われない（スパム扱いされない）配信システムの構築</h4>
<span class="boxTxt mgB15">オートビズは、スパム判定アルゴリズムを日々研究し、そのノウハウを活かした「受信サーバーに嫌われない配信システム」の構築に成功しています。（このノウハウは企業秘密です）</span>
<p class="boxTxt mgB15">
もちろん、スパム判定はメールの内容（言葉、URL）や送信者メールアドレス等にも依存しますので「絶対に届く」とは言い切れませんが、配信状況を常にモニタリングしながら配信の最適化を行なっています。</p>

<h4 class="boxTtlS">ユーザーさまの協力のもと『配信の質』を維持し、スパマーを徹底排除</h4>
<p class="boxTxt mgB15">「厳しすぎるのでは...」と言われるほど、当サービスの利用規約やポリシーは“スパム的な配信をする方”には厳しい内容になっています。&gt; <a href="{{ url("../information/policy.html") }}">ポリシーと利用規約</a></p>
<p class="boxTxt mgB15">正しくご利用いただく方にとっては、安心してご利用いただける規約となっています。ユーザーさまにもご協力いただき、しっかりと配信の同意を得て配信いただくなど、スパムメール扱いされないような配信を心がけていただいています。</p>
<p class="boxTxt mgB15">スパムメールや迷惑行為に対しては厳しく対処し、サービスの信頼性を維持しています。ですから迷惑メールフォルダに入りづらく、高い到達率が実現できているのです。</p>
