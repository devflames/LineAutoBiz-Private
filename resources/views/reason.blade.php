<h3 class="boxTtl">「シームレスな情報発信で資産構築」LINE×メール</h3>
<p class="boxTxt mgB15">LINE公式アカウントとオートビズを連携させることで、LINEとメールをオートビズからシームレスに送ることができます。</p>
<p class="boxTxt mgB15">また、「メールアドレス」と「友だち（LINEアカウント）」を一元管理ができるのもオートビズならでは。</p>
<p class="boxTxt mgB15">例えば、「Aさんのメールアドレス」と「AさんのLINEアカウント」は別々なものなので、こちらにはわかりません。しかしオートビズを使うことで紐づけが可能になり、用途に合わせてメールとLINE使い分けながらからAさんにメッセージを送ることができます。</p>
<p class="boxTxt mgB15">そして、何よりのメリットは、LINE公式アカウントのBAN対策になるということ。<br>
メールはインターネット上のインフラなのでなくなりません。仮にLINE公式アカウントがBANされてしまったとしてもメールで連絡が取れるので、築きあげてきた関係性とリスト（資産）を失うことがなく、ビジネスを続けることができます。<br>
LINEというプラットフォームだけに依存せず、資産構築ができるのが最大の強みです。</p>
