<h2 class="popTtl_icon"><span><img src="{{ asset("/images/icon_list.gif") }}" width="40" height="40" alt=""/></span><em>その他の機能一覧</em></h2>


<dl class="list_Box list_Box1">
<dt>クリックカウント機能</dt>
<dd>URLを誰が、何回クリックしたかを計測できます。クリック数を見ることで、読者がどんなことに興味を持っているのか調べられます。月別、日別、曜日別等、細かく集計できるので、メール配信をするうえで反応のいい時間帯を調べることもできます。<br>
<span class="note">※スーパーライトプランはご利用いただけません</span></dd>
</dl>

<dl class="list_Box">
<dt>メディア管理機能</dt>
<dd>弊社が提供するファイルサーバーに、画像は200MBまで、PDFや音声等のファイルは200MBまでで合計400MBアップロードできます。
アップロードしたファイルは、公開制限（パスワード、有効期間、ダウンロード上限）を設けることができるので、色んな活用の仕方があります。
もちろん、画像をアップロードして、フォームやHTMLメールに表示させてもOK！</dd>
</dl>


<dl class="list_Box">
<dt>配信管理機能</dt>
<dd>各登録者の配信状況（次回配信日時・ステップ、エラーなど）がひと目でわかり、個別に配信の停止・再開・解除も行えます。</dd>
</dl>


<dl class="list_Box">
<dt>リスト管理機能</dt>
<dd>登録者データの検索や、任意のステップ・開始日時を指定して新規登録、登録者データや配信スケジュールの個別変更が行えます。<br>
データはCSV形式でダウンロード可能です。ダウンロードにパスワード制限をかけることもできます。
</dd>
</dl>


<dl class="list_Box">
<dt>リスト一括登録機能</dt>
<dd>お手持ちのCSVリストを一括登録！登録後の状態（停止状態で登録等）、開始ステップや開始日時も自由に設定いただけます。 他のステップメール（シナリオ）の登録者データもコピー/移動できます。<br>
<span class="note">※スーパーライトプランはご利用いただけません</span></dd>
</dl>


<dl class="list_Box">
<dt>コピー機能</dt>
<dd>一度作ったフォームやメールは、設定のコピーができます。</dd>
</dl>


