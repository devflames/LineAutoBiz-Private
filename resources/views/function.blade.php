<h2 class="popTtl_icon"><span><img src="{{ asset("/images/icon_mail.gif") }}" width="40" height="40" alt=""/></span><em><a name="step" class="anchor">ステップメール配信</a></em></h2>

<dl class="list_Box list_Box1">
<dt>ステップメール配信</dt>
<dd>あらかじめ設定しておいたメールを、指定した間隔で自動で送ることができます。毎日決まった時刻に送ったり、決まった曜日・日付に配信もできます。</dd>
</dl>

<dl class="list_Box">
<dt>ステップ数</dt>
<dd>1つのシナリオ内に作れるメールのステップ数は【無制限】です。<br><br>
<span class="note">[シナリオとは？]<br>
『登録者に何通のメールをどのような配信間隔で送るのか』 といったメール配信の一連の流れ（複数通のメール）のことを言います。</span></dd>
</dl>

<dl class="list_Box">
<dt>入力内容自動差込</dt>
<dd>件名や本文にお名前やメールアドレス等、フォームに入力された内容を差し込めます。</dd>
</dl>

<dl class="list_Box">
<dt>配信スケジュール設定</dt>
<dd>日付や曜日など配信時期を細かく指定可能です。ステップメール配信の効果を最大化できます。</dd>
</dl>


<dl class="list_Box">
<dt>配信待機（自動一時停止）</dt>
<dd>任意のステップで配信を自動で一時停止、 配信再開ボタンを押すだけでステップメールを再開できます。</dd>
</dl>


<dl class="list_Box">
<dt>リピート配信</dt>
<dd>全ステップ配信終了後に、任意のステップに戻って繰り返しステップメール配信させることができます。</dd>
</dl>


<dl class="list_Box">
<dt>別シナリオへの接続</dt>
<dd>全ステップ配信終了後に、別シナリオのステップメールに自動接続できます。</dd>
</dl>


<dl class="list_Box">
<dt>定型文挿入</dt>
<dd>登録しておいた定型文を任意のステップの任意の場所に挿入できます。期間限定のキャンペーンなど、タイムリーな情報をステップメール内で告知することができます。</dd>
</dl>


<dl class="list_Box">
<dt>配信解除URL差し込み</dt>
<dd>読者側で配信解除ができる解除URLをメール内に差し込むことができます。</dd>
</dl>


<dl class="list_Box">
<dt>HTMLメール配信</dt>
<dd>文字の色やサイズを変えたり、画像を表示させたり、HTML形式のメールを送れます。<br>
テキスト形式メールも用意しておくことで、HTML形式メールを表示できない環境の人には、テキスト形式で表示される「マルチパート形式」を採用しています。<br>
<span class="note">※デコメールの配信には対応していません。</span></dd>
</dl>


<dl class="list_Box">
<dt>開封率測定</dt>
<dd>ステップごとの開封率と読者ごとの開封数を確認することができます。<br>
<span class="note">※計測期間はステップメール配信から30日間となります。</span></dd>
</dl>


<dl class="list_Box">
<dt>配信シミュレーション</dt>
<dd>設定したステップメールが、「いつ登録すれば実際にどんなスケジュール（日付）で配信されるのか」をシミュレーションできます。</dd>
</dl>


<dl class="list_Box">
<dt>エラーメール自動処理</dt>
<dd>エラー内容によって解除・停止を自動で処理します。エラーメールを受信するアドレスを指定することもできます。</dd>
</dl>


<dl class="list_Box">
<dt>多言語対応</dt>
<dd>メール配信の文字コードをISO-2022-JP、Unicode(UTF-8）から選択できますので、中国語など、他の言語でのメールも送れます。</dd>
</dl>
